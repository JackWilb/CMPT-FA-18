USE SaleCo;
SELECT LINE_NUMBER, LINE.P_CODE, P_DESCRIPT, LINE_UNITS, LINE_PRICE, INV_DATE
FROM LINE 
JOIN PRODUCT ON LINE.P_CODE = PRODUCT.P_CODE
JOIN INVOICE ON LINE.INV_NUMBER = INVOICE.INV_NUMBER
JOIN CUSTOMER ON CUSTOMER.CUS_CODE = INVOICE.CUS_CODE
WHERE CUS_FNAME = 'Myron' AND CUS_LNAME = 'Orlando';